static bool taiko_is_digital_gain_register(unsigned int reg)
{
	bool rtn = false;
	switch (reg) {
	case WCD9320_A_CDC_RX1_VOL_CTL_B2_CTL:
	case WCD9320_A_CDC_RX2_VOL_CTL_B2_CTL:
	case WCD9320_A_CDC_RX3_VOL_CTL_B2_CTL:
	case WCD9320_A_CDC_RX4_VOL_CTL_B2_CTL:
	case WCD9320_A_CDC_RX5_VOL_CTL_B2_CTL:
	case WCD9320_A_CDC_RX6_VOL_CTL_B2_CTL:
	case WCD9320_A_CDC_RX7_VOL_CTL_B2_CTL:
	case WCD9320_A_CDC_TX1_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX2_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX3_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX4_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX5_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX6_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX7_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX8_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX9_VOL_CTL_GAIN:
	case WCD9320_A_CDC_TX10_VOL_CTL_GAIN:
		rtn = true;
		break;
	default:
		break;
	}
	return rtn;
}

static bool wcd9320_is_volatile_register(struct device *dev, unsigned int reg)
{
	int i;

	/* Registers lower than 0x100 are top level registers which can be
	 * written by the Taiko core driver.
	 */

	if ((reg >= WCD9320_A_CDC_MBHC_EN_CTL) || (reg < 0x100))
		return 1;

	/* IIR Coeff registers are not cacheable */
	if ((reg >= WCD9320_A_CDC_IIR1_COEF_B1_CTL) &&
		(reg <= WCD9320_A_CDC_IIR2_COEF_B2_CTL))
		return 1;

	/* ANC filter registers are not cacheable */
	if ((reg >= WCD9320_A_CDC_ANC1_IIR_B1_CTL) &&
		(reg <= WCD9320_A_CDC_ANC1_LPF_B2_CTL))
		return 1;
	if ((reg >= WCD9320_A_CDC_ANC2_IIR_B1_CTL) &&
		(reg <= WCD9320_A_CDC_ANC2_LPF_B2_CTL))
		return 1;

	/* Digital gain register is not cacheable so we have to write
	 * the setting even it is the same
	 */
	if (taiko_is_digital_gain_register(reg))
		return 1;

	/* HPH status registers */
	if (reg == WCD9320_A_RX_HPH_L_STATUS || reg == WCD9320_A_RX_HPH_R_STATUS)
		return 1;

	if (reg == WCD9320_A_MBHC_INSERT_DET_STATUS)
		return 1;

	switch (reg) {
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL0:
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL1:
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL2:
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL3:
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL4:
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL5:
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL6:
	case WCD9320_A_CDC_SPKR_CLIPDET_VAL7:
	case WCD9320_A_CDC_VBAT_GAIN_MON_VAL:
		return 1;
	}

	return 0;
}

